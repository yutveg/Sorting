function mergeHelper(arrA, arrB){
    //takes in two sorted arrays
    master = [null * (arrA.length + arrB.length)]
    indexA = 0
    indexB = 0
    indexMaster = 0
    while (indexA < arrA.length && indexB < arrB.length){
        // iterate through, sorting smallest first into master
        if (arrA[indexA] >= arrB[indexB]){
            master[indexMaster] = arrB[indexB]
            indexB += 1
            indexMaster += 1
        }
        else if (arrA[indexA] < arrB[indexB]){
            master[indexMaster] = arrA[indexA]
            indexA += 1
            indexMaster += 1
        }
    }

    while (indexA < arrA.length){
        master[indexMaster] = arrA[indexA]
        indexA += 1
        indexMaster += 1
    }

    while (indexB < arrB.length){
        master[indexMaster] = arrB[indexB]
        indexB += 1
        indexMaster += 1
    }

    return master
}

function mergeSort(arr){
    if (arr.length === 1){
        return arr
    }
    let arrA = arr.slice(0, arr.length / 2)
    let arrB = arr.slice(arr.length / 2)
    arrA = mergeSort(arrA)
    arrB = mergeSort(arrB)
    return mergeHelper(arrA, arrB)
}

